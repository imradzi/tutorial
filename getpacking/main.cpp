std::tuple<double, std::string> GetPacking(const std::string &name) {
    auto fnIsNumber = [](const char a) {
        return a >= '0' && a <= '9';
    };

    std::string desc;
    int descStart = 0;
    int packsizeStart = 0;
    for (int i = name.length() - 1; i >= 0; i--) {
        if (fnIsNumber(name.at(i)) && desc.empty()) {
            desc = name.substr(i + 1);
            descStart = i + 1;
        } else if (!fnIsNumber(name.at(i)) && !desc.empty()) {
            packsizeStart = i + 1;
            break;
        }
    }
    if (packsizeStart > 0) {
        double packsize = std::stod(name.substr(packsizeStart, descStart - packsizeStart + 1));
        return {packsize, desc};
    } else
        return {0, ""};
}

